# --- Week 1 Exercise: Risk Assessment ---
# Your goal is to write a script that assesses a risk score.

# 1. We've started with a variable for you.
risk_score = 82

print(f"Assessing risk for a score of: {risk_score}")

# 2. Your task:
#    - Write an if/else statement.
#    - If the risk_score is greater than 75, it should print "High Risk".
#    - Otherwise (else), it should print "Acceptable Risk".

# --- WRITE YOUR IF/ELSE BLOCK BELOW ---
if risk_score > 75:
    print("High Risk")
else:
    print("Acceptable Risk")

# -----------------------------------------

print("Assessment complete.")
